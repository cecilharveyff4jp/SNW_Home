// メッセージ生成関数
export function generateOmikujiMessage(level: number): string {
  const isGood = level >= 16;
  const messages = isGood ? GOOD_MESSAGES : BAD_MESSAGES;
  
  const prefix = messages.prefix[Math.floor(Math.random() * messages.prefix.length)];
  const subject = messages.subject[Math.floor(Math.random() * messages.subject.length)];
  const verb = messages.verb[Math.floor(Math.random() * messages.verb.length)];
  const object = messages.object[Math.floor(Math.random() * messages.object.length)];
  const suffix = messages.suffix[Math.floor(Math.random() * messages.suffix.length)];
  
  // メイン文章の最後に「。」をつけて、改行してsuffixを最後に配置
  let message = `${prefix}${subject}${verb}${object}。\n\n${suffix}`;
  
  // レベル10以下の場合は追加の悪い出来事
  if (level <= 10) {
    const extraEvent = EXTRA_BAD_EVENTS[Math.floor(Math.random() * EXTRA_BAD_EVENTS.length)];
    message += `\n\nさらに...${extraEvent}`;
  }
  
  return message;
}

// ラッキーアイテム取得関数
export function getLuckyItem(): LuckyItem {
  return LUCKY_ITEMS[Math.floor(Math.random() * LUCKY_ITEMS.length)];
}

// おみくじ結果取得関数
export function drawOmikuji(): {
  result: OmikujiResult;
  message: string;
  luckyItem: LuckyItem;
} {
  // ランダムに運勢を選択（完全ランダム）
  const result = FORTUNES[Math.floor(Math.random() * FORTUNES.length)];
  const message = generateOmikujiMessage(result.level);
  const luckyItem = getLuckyItem();
  
  return { result, message, luckyItem };
}
